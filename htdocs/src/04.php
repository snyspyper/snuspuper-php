<h3>Альтернативный синтаксис управляющих структур</h3>
PHP предланает альтернативный синтаксис для некоторых конструкций: if, while, for, foreach и switch.<br>
Начинается с двоеточием, а заканчивается endif, endwhile, endfor, endforeach и endswitch соответственно.<br>
<?php $a = 8;?>
<pre><--Source-->
<?php if ($a > 5):?>
Число больше пяти.
<?php else:?>
Число меньше пяти.
<?php endif;?>
<--Source--></pre>




















































